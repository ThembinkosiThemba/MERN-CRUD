import mongoose from 'mongoose';

const foodSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var FoodMessage = mongoose.model('FoodMessage', foodSchema);

export default FoodMessage;